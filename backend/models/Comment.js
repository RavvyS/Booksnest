const mongoose = require("mongoose");

const commentSchema = new mongoose.Schema({

  content: {
    type: String,
    required: true,
  },

  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },

  materialId: {
    type: String,
    required: true,
  },

  createdAt: {
    type: Date,
    default: Date.now,
  },

  updatedAt: {
    type: Date,
  },

});

module.exports = mongoose.model("Comment", commentSchema);
